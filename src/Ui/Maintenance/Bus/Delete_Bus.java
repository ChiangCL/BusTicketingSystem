    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (validation() == true) {
            int n = JOptionPane.showConfirmDialog(null, "Are you sure want to delete it?",
                    "RECORD DELETED", JOptionPane.OK_CANCEL_OPTION);
            if (n == JOptionPane.OK_OPTION) {
                busControl.DeleteRecord(bus);
                busId.removeAllItems();
                busId.addItem("-");
            }
            try {
                String sql = "select * from BUS";
                stmt = conn.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();
                while (rs.next()) {

                    String id = rs.getString(1);
                    busId.addItem(id);

                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            busno.setText(null);
            seatno.setText(null);
            buscombo.setText(null);
            staffcombo.setText(null);
            jButton1.setEnabled(false);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

