    public boolean validation() {
        if ((busId.getSelectedItem() == "-")) {
            JOptionPane.showMessageDialog(null, "Please select the bus id",
                    "Invalid selection", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        if ((busId.getSelectedItem() == "-")) {
            JOptionPane.showMessageDialog(null, "Please select the bus id",
                    "Invalid selection", JOptionPane.ERROR_MESSAGE);
        } else {
            String id = String.valueOf(busId.getSelectedItem());
            bus = busControl.selectRecord(id);
            jButton1.setEnabled(true);
            busno.setText(bus.getBUS_NO());
            seatno.setText(String.valueOf(bus.getSEAT()));
            buscombo.setText(bus.getBUS_MODEL());
            staffcombo.setText(bus.getSTAFF_ID());
        }
    }//GEN-LAST:event_searchActionPerformed

