    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id = BusID.getText();
        String bus_no = busno.getText().toUpperCase();

        String model = String.valueOf(buscombo.getSelectedItem());
        String staff_id = String.valueOf(staffcombo.getSelectedItem());

        if (validation() == true) {
            int seat = Integer.parseInt(seatno.getText());
            Bus bus = new Bus(id, bus_no, seat, model, staff_id);
            busControl.addRecord(bus);
            JOptionPane.showMessageDialog(null, "Bus added successfully.",
                    "Bus Added", JOptionPane.INFORMATION_MESSAGE);
            BusID.setText(busControl.GetNewBusCode());
            busid.setText(busControl.GetNewBusCode());
            busno.setText(null);
            seatno.setText(null);
            buscombo.setSelectedItem("-");
            staffcombo.setSelectedItem("-");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

