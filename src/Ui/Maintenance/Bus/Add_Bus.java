    public boolean validation() {

        if (busno.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Please enter the Bus No. "
                    + "eg.ABC1234",
                    "Invalid Bus No", JOptionPane.ERROR_MESSAGE);
            busno.grabFocus();
            return false;
        } else if ((busno.getText().trim().length() > 7)) {
            JOptionPane.showMessageDialog(null, "Please enter not more than 7  alphanumeric in Bus No. "
                    + "eg.ABC1234",
                    "Invalid Format Bus No", JOptionPane.ERROR_MESSAGE);
            busno.grabFocus();
            return false;
        } else if ((busno.getText().trim().length() < 7)) {
            JOptionPane.showMessageDialog(null, "Please enter not less than 7  alphanumeric in Bus No. "
                    + "eg.ABC1234",
                    "Invalid Format Bus No", JOptionPane.ERROR_MESSAGE);
            busno.grabFocus();
            return false;
        } else if ((seatno.getText().trim().length() == 0)) {
            JOptionPane.showMessageDialog(null, "Please enter the Number of Seat. eg.25",
                    "Invalid Seat number", JOptionPane.ERROR_MESSAGE);
            seatno.grabFocus();
            return false;
        } else if ((seatno.getText().trim().length() > 2)) {
            JOptionPane.showMessageDialog(null, "Please not more than 2 numeric. eg.25",
                    "Invalid Format", JOptionPane.ERROR_MESSAGE);
            seatno.grabFocus();
            return false;
        } else if ((seatno.getText().trim().length() < 2)) {
            JOptionPane.showMessageDialog(null, "Please not less than 2 numeric. eg.25",
                    "Invalid Format", JOptionPane.ERROR_MESSAGE);
            seatno.grabFocus();
            return false;
        } else if ((buscombo.getSelectedItem() == "-")) {
            JOptionPane.showMessageDialog(null, "Please select the bus model.",
                    "Invalid selection", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if ((staffcombo.getSelectedItem() == "-")) {
            JOptionPane.showMessageDialog(null, "Please select the staff id.",
                    "Invalid selection", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

